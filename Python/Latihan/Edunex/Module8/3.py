# Deskripsi : Program menghitung jarak (s) berdasarkan kecepatan (v) dan waktu tempuh (t); input: kecepatan, waktu; output: jarak

# KAMUS
# s, t, v: Float

def jarak(v,t):
    '''deskripsi fungsi'''
    # KAMUS LOKAL
    # v,t: Float
    return v*t

# ALGORITMA PROGRAM UTAMA
print(jarak(2,3))